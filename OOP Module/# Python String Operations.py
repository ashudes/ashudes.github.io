# Python String Operations
str1 = 'Hello'
str2 ='World!'

# using +
result = str1 + str2
print('str1 + str2 = ', result)
assert result == 'HelloWorld!', "Check the operation: str1 + str2"

# using *
result = str1 * 3
print('str1 * 3 =', result)
assert result == 'HelloHelloHello', "Check the operation: str1 * 3"